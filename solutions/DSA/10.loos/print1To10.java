public class print1To10 {
    public static void main(String[] args) {


        //for loop
        for (int i = 1; i <= 10; i++) {
           System.out.println(i); 
        }

        // while loop
        int j = 1;
        while (j<=10) {
            System.out.println(j);
            j++;
        }


        // do while loo[]
        int k = 1;
        do {
            System.out.println(k);
            k++;
        } while (k<=10);
        
    }
    
}
